const fromInput = document.querySelector(".currency-from input");
const fromSelect = document.querySelector(".currency-from select");

const toInput = document.querySelector(".currency-to input");
const toSelect = document.querySelector(".currency-to select");

let rate;

let optionsHTML = Object.entries(symbols).reduce((acc,e)=>{
    return acc + `<option value="${e[0]}">${e[1]}</option>`;
},"");

fromSelect.innerHTML = optionsHTML;
toSelect.innerHTML = optionsHTML;

fromSelect.value = "EGP";
toSelect.value = "KES";

fromInput.addEventListener("input",(e)=>{
    let v = 1;
    if(e.target.value.trim() !== ""){
        v = parseFloat(e.target.value.trim());
    }
    toInput.value = Math.trunc(rate * v * 100) / 100;
});

toInput.addEventListener("input",(e)=>{
    let v = 1;
    if(e.target.value.trim() !== ""){
        v = parseFloat(e.target.value.trim());
    }
    fromInput.value = Math.trunc((1/rate) * v * 100) / 100;
});

function getExchange(){
    let v1 = fromSelect.value;
    let v2 = toSelect.value;

    fetch(`https://api.apilayer.com/exchangerates_data/convert?to=${v2}&from=${v1}&amount=1`,{
        method: "GET",
        headers:{
            "apikey":"0rZJ4bhKwliSjFMdXf66h7NXOIDDDXqv"
        }
    }).then((v)=>{
        return v.json();
    }).then((res)=>{
        rate = res.info.rate;
        let v = fromInput.value.trim();
        if(v !== ""){
            v = parseFloat(v);
        } else {
            v = 1;
        }
        toInput.value = rate * v;
    });
}

getExchange();